# GRADE OF A STUDENT

marks = int(input("enter your marks :"))
if(marks >=90) :
    print("EXCELLCENT")
elif(marks >= 80 and marks < 90) :
    print("A")
elif(marks >= 70 and marks <80) :
    print("B")
elif(marks >= 60 and marks < 70) :
    print("C")
elif(marks >=50 and marks< 60) :
    print("D")
elif(marks >=40 and marks <50) :
    print("E")
elif(marks >=30 and marks <40) :
    print("F")
else:
    print("fail")